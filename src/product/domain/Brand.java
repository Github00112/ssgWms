package product.domain;

public class Brand {
    // 브랜드 id
    private Long id;
    // 브랜드 이름
    private String name;
    // 브랜드 코드

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



}
